
public class Switch02 {
public static void main(String[] args) {
	String month="january";
	switch(month) {
	case "january":System.out.println("january is first month");
	break;
	case"february":System.out.println("february is second month");
	break;
	case"march":System.out.println("march is third month");
	break;
	case"april":System.out.println("april is fourth month");
	break;
	case"may":System.out.println("may is fifth month");
	break;
	case"june":System.out.println("june is sixth month");
	break;
	case"july":System.out.println("july is seventh month");
	break;
	case"auguest":System.out.println("auguest is eight month");
	break;
	case"september":System.out.println("september is nineth month");
	break;
	case"november":System.out.println("november is tenth month");
	break;
	case"desember":System.out.println("desember is eleven month");
	break;
	case"october":System.out.println("october is twelve month");
	break;
	default:System.out.println("invalid");
	
	}
	
	}

}
