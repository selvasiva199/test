package Oops1;

public class Car {
	private String brand;
	private int price;
	private Engine engine;
	
	public void setBrand(String brand) {
		this.brand=brand;
	}
	public void setPrice(int price) {
		this.price=price;
	}
	public void setEngine(Engine engine) {
		this.engine=engine;
	}
	public String getBrand() {
		return brand;
	}
	public int getPrice() {
		return price;
	}
	public Engine getEngine() {
		return engine;
	}


	}


